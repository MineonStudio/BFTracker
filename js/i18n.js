// 当前语言
let currentLang = localStorage.getItem('bf-lang') || 'zh';

// 获取翻译
function t(key) {
  return I18N[currentLang]?.[key] || I18N['zh']?.[key] || key;
}

// 切换语言
function toggleLang() {
  currentLang = currentLang === 'zh' ? 'en' : 'zh';
  localStorage.setItem('bf-lang', currentLang);
  var b = document.getElementById('langToggle');
  if (b) b.textContent = currentLang === 'zh' ? 'EN' : '中文';
}
function applyLang() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = currentLang === 'zh' ? 'EN' : '中文';
  document.querySelectorAll('[data-i18n-text]').forEach(el => {
    el.textContent = t(el.getAttribute('data-i18n-text'));
  });
}

// 初始化
setTimeout(() => {
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = currentLang === 'zh' ? 'EN' : '中文';
}, 100);

window.t = t;
window.toggleLang = toggleLang;
